age = int(input("Wat is je leeftijd?: "))
if age <= 17:
    print("Je bent helaas niet oud genoeg om te rijden!")

if age >= 18:
    print("Je bent oud genoeg om auto te rijden, nu wel nog je rijbewijs!")


